public class Variaveis {
    private String Tipo;
    private String Nome;
    private String Valor;
    public void setTipo(String v)
    {
    this.Tipo = v;
    }
    public void setNome(String v)
    {
    this.Nome = v;
    }
    public void setValor(String v)
    {
    this.Valor = v;
    }
    public String getNome()
    {
    return this.Nome;
    }
    public String getTipo()
    {
    return this.Tipo;
    }
    public String getValor()
    {
    return this.Valor;
    }
    }